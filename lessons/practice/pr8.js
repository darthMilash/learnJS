// Место для первой задачи
function getTimeFromMinutes(min) {
    if (min >= 0 && typeof(min) == 'number' && Number.isInteger(min)) {
        const hour = parseInt(min/60);
        const minutes = min - parseInt(hour)*60;
        if (hour >= 2 && hour <= 4) {
            return `Это ${hour} часа и ${minutes} минут`;
        }
        else if (hour === 1) {
            return `Это ${hour} час и ${minutes} минут`;
        }
        else return `Это ${hour} часов и ${minutes} минут`;

        
    }
    else return 'Ошибка, проверьте данные';
}

