function findMaxNumber(a, b, c, d) {
    if (typeof(a) == 'number' && typeof(b) == 'number' && typeof(c) == 'number' && typeof(d) === 'number') {
        if (a >= b && a >= c && a >= d) {
            return a;
        }
        else if (b >= a && b >= c && b >= d) { 
            return b;
        }
        else if (c >= a && c >= b && c >= d) {
            return c;
        }
        else if (d >= a && d >= b && d >= c) {
            return d;
        }

    }
    else return (0);
}

